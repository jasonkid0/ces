              <p>Saint Francis of Assisi College had its beginnings in 1981, when the husband and wife team of Dr. Evangeline Oñas-Orosco and Dr. Arturo Agapito Orosco, Sr., opened a two-classroom preparatory school in Talon III, Las Piñas City to provide affordable quality education for young children. By 1983, the school’s popularity was such that a two-storey, full elementary school building had to be constructed to meet the surrounding community needs. The opening of the high school department quickly followed in 1986, assuring placements for SFAC graduates in the secondary level. At the same time,  two new campuses were established, one in Taguig City, and one in Los Baños, Laguna.</p>
              <p class="mb-0">In 1989, the tertiary level in SFAC came into being offering several courses both in degree and non-degree programs. New campuses have since been added: the Pamplona, Las  Piñas campus in 1990, the Bacoor, Cavite campus in 1994, the Alabang, Muntinlupa campus in 1996. In the same year, 1996, Saint Anthony School of Las Piñas came under SFAC supervision and management. Dasmariñas, Cavite campus opened in 1999. The Biñan campus, in Laguna opened in 2006. In 2010, the Los Baños campus was renamed Saint Anthony School of Los Baños, named after the patron saint of the locality. The 10th campus, located in Sta. Rosa, Laguna accepts enrollees starting S.Y. 2012 – 2013. In line with corporate branding moves,  the Pamplona campus, being the smallest campus, moved its operation to the main campus in 2014.</p>
              <p>Dr. Aturo O. Orosco, Jr., succeeding his mother as school president in 2012, steered the school system into a brand where bayanihan spirit cultivates the deep heritage of the school. Bold moves and decisions are continuously being made to meet the new leadership’s branding innovations and to meet the founders’ objective of serving more communities, and societies, at large.</p>
